// 2 Підрахунок суми парних чисел:
// Створіть код, який використовує цикл while для знаходження суми всіх парних чисел в заданому діапазоні (наприклад, від 1 до 20).


let counter = 0;
let sum = 0;

do {
    if (counter % 2 === 0) {
        sum += counter;
    }
    counter++;

} while (counter <= 20);

console.log(`${sum}`);